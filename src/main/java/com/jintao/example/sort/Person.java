public class Person {
    private int age;
    private int height;

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getHeight() {
        return height;
    }

    public Person(int age, int height) {
        this.age = age;
        this.height = height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
